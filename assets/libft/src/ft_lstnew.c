#include "libft.h"

t_list	*ft_lstnew(char *content)
{
	t_list	*new_list;

	new_list = malloc(sizeof(t_list));
	if (!new_list)
		return (NULL);
	new_list->content = ft_strdup(content);
	new_list->next = NULL;
	return (new_list);
}
